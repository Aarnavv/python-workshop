# for loop
beatles = "Norwegian Wood"

# Definite Iteration
for i in range(10, 30):
    print(i, end=" ")

print()

for i in beatles:
    print(i, end=" ")

# while loop

i = 1
while i < 6:
    print(i)
    i += 1

i = 1
while i < 6:
    print(i)
    i += 1
else:
    print("i is no longer less than 6")
